int16_t find_mac_addr(ble_gap_addr_t ble_addr) {
  int16_t i;
  for (i=0; i<sizeof(master_addr_list); i++) {
    if (memcmp(master_addr_list[i].addr,
               (uint8_t*)&ble_addr.addr,
               BLE_GAP_ADDR_LEN) == 0) break;
    }
  if (i >= sizeof(master_addr_list)) return -1;
  return i+1;
  }
